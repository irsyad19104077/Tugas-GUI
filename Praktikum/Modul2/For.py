# For
#Menggunakan variabel
angka = 10
for i in range(angka) :
    print("Angka ke : " ,i)

#Pengurangan
angka = 10
for i in range(angka, -1, -2) :
    print("Angka ke : " ,i)

#Max-Min
angka = 10

# Param 1 : Maximal
for i in range(angka) :
    print("Angka ke : ", i)
# Param 2 : Minim, Max(sifat increment
for i in range(angka, 20) :
    print("Angka ke : ", i)
# Param 3 : Max,Min, Lompatan - (sifat decre)
# Param 3 : Min,Max, Lompatan + (sifat incre)
for i in range(angka, 1, -1) :
    print("Angka ke : " ,i)